#!/usr/bin/env python
"""
This script generates a protocol text file for GeneLab Remove-Host-Reads workflow.
It reads software versions from a text file generated upstream, 
and assembly info from a manifest.txt file generated by k2 download-library (if available in the database directory), 
or from assembly name and accession parameters passed in from Nextflow (resolved upstream from workflow params or hosts.csv). 
If neither source is available, assembly info defaults to 'unknown'. 
"""

import argparse
import os
import re


# ---------------------------------------------------------------------------
# Hardcoded Kraken2 build parameters
# ---------------------------------------------------------------------------
KMER_LEN      = 35
MINIMIZER_LEN = 31
NO_MASKING    = True


# ---------------------------------------------------------------------------
# Kraken2 version from software_versions.txt
# ---------------------------------------------------------------------------

def parse_kraken2_version(software_versions_path):
    """
    Parse Kraken2 version from software_versions.txt.
    Expects a line like: Kraken2 2.1.6
    """
    if not software_versions_path or not os.path.exists(software_versions_path):
        print(f"[WARNING] software_versions.txt not found at '{software_versions_path}'. "
              "Version will be reported as 'unknown'.")
        return "unknown"

    with open(software_versions_path, "r") as f:
        for line in f:
            match = re.search(r"[Kk]raken2?\s+v?([\d.]+)", line)
            if match:
                return match.group(1)

    print("[WARNING] Could not find Kraken2 version in software_versions.txt.")
    return "unknown"


# ---------------------------------------------------------------------------
# Assembly from manifest.txt
# ---------------------------------------------------------------------------

def assemblies_from_manifest(manifest_path):
    """
    Parse manifest.txt to extract accession(s) and assembly name(s).

    Each line looks like:
    genomes/all/GCF/009/914/755/GCF_009914755.1_T2T-CHM13v2.0/GCF_009914755.1_T2T-CHM13v2.0_genomic.fna.gz
    """
    assemblies = []
    with open(manifest_path, "r") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            filename = os.path.basename(line)
            match = re.match(r"(GCF_\d+\.\d+)_(.+?)_genomic\.fna\.gz", filename)
            if match:
                assemblies.append({
                    "accession":     match.group(1),
                    "assembly_name": match.group(2)
                })
            else:
                print(f"[WARNING] Could not parse manifest line: {line}")

    return assemblies if assemblies else None


# ---------------------------------------------------------------------------
# Protocol generation
# ---------------------------------------------------------------------------

def format_assembly_list(assemblies):
    """
    Format assembly list for protocol text.
    e.g. GRCh38.p14 (GCF_000001405.40) and T2T-CHM13v2.0 (GCF_009914755.1)
    """
    parts = [f"{a['assembly_name']} ({a['accession']})" for a in assemblies]
    if len(parts) == 1:
        return parts[0]
    elif len(parts) == 2:
        return f"{parts[0]} and {parts[1]}"
    else:
        return ", ".join(parts[:-1]) + f", and {parts[-1]}"


def generate_protocol(assemblies, host, kraken2_version):
    """Generate the protocol statement with hardcoded build parameters."""
    assembly_str = format_assembly_list(assemblies)
    masking_str  = ", with --no-masking" if NO_MASKING else ""

    return (
        f"FASTQ files were screened using Kraken2 v{kraken2_version} against a {host} genome reference database "
        f"that was constructed from NCBI's RefSeq {assembly_str}{masking_str}, "
        f"and defaults of kmer-length {KMER_LEN} and minimizer-length of {MINIMIZER_LEN}, "
        f"and is fully compatible with Kraken2 v{kraken2_version}. "
        f"Reads classified as host were removed after being quantified and expressed as a percentage of the total."
    )


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(
        description="Generate a Kraken2 host depletion protocol statement."
    )
    parser.add_argument("--db-dir",            required=True,
                        help="Path to kraken2-<host>-db/ directory")
    parser.add_argument("--software-versions", required=True,
                        help="Path to software_versions.txt")
    parser.add_argument("--host",            required=True,
                        help="Host organism of interest")
    parser.add_argument("--assembly-name",     default="",
                        help="Assembly name resolved upstream in Nextflow (e.g. GRCm39)")
    parser.add_argument("--assembly-acc",      default="",
                        help="Assembly accession resolved upstream in Nextflow (e.g. GCF_000001635.27)")
    parser.add_argument("--output",            default="protocol.txt",
                        help="Output file path (default: protocol.txt)")

    args = parser.parse_args()

    # Parse Kraken2 version
    kraken2_version = parse_kraken2_version(args.software_versions)
    print(f"[INFO] Kraken2 version: {kraken2_version}")

    # Resolve assembly info
    assemblies = None

    manifest_path = os.path.join(args.db_dir, "manifest.txt")

    if os.path.exists(manifest_path):
        # Priority 1: manifest.txt in db_dir
        assemblies = assemblies_from_manifest(manifest_path)
    elif args.assembly_name and args.assembly_acc:
        # Priority 2: assembly name + accession passed in from Nextflow
        assemblies = [{"accession": args.assembly_acc, "assembly_name": args.assembly_name}]
    else:
        # Priority 3: fallback to unknown
        print("[WARNING] Could not resolve assembly info from manifest.txt or params. "
            "Defaulting to 'unknown'.")
        assemblies = [{"accession": "unknown", "assembly_name": "unknown"}]


    # Generate and save protocol
    protocol = generate_protocol(assemblies, args.host, kraken2_version)

    with open(args.output, "w") as f:
        f.write(protocol + "\n")

    print(f"\n[PROTOCOL]\n{protocol}")
    print(f"\n[INFO] Protocol saved to: {args.output}")


if __name__ == "__main__":
    main()
